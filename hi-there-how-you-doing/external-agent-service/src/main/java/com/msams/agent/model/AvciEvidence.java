package com.msams.agent.model;

public class AvciEvidence {

    private String attributable;
    private String verifiable;
    private String classifiable;
    private String improvable;

    public String getAttributable() {
        return attributable;
    }

    public void setAttributable(String attributable) {
        this.attributable = attributable;
    }

    public String getVerifiable() {
        return verifiable;
    }

    public void setVerifiable(String verifiable) {
        this.verifiable = verifiable;
    }

    public String getClassifiable() {
        return classifiable;
    }

    public void setClassifiable(String classifiable) {
        this.classifiable = classifiable;
    }

    public String getImprovable() {
        return improvable;
    }

    public void setImprovable(String improvable) {
        this.improvable = improvable;
    }
}
